//Greater Number
#include<iostream>
using namespace std;
int main(){
    int x,y;
    cout<<"Enter your first number:";
    cin>>x;
    cout<<"Enter your second number:";
    cin>>y;
    if (x>y)
    {
      cout<<"The greater number is:"<<x;
    }
    else{
        cout<<"The greater number is:"<<y;
    }
}